local function urlencodeChar(char)
    return "%" .. string.format("%02X", string.byte(c))
end

function string.urlencode(str)
    -- convert line endings
    str = string.gsub(tostring(str), "\n", "\r\n")
    -- escape all characters but alphanumeric, '.' and '-'
    str = string.gsub(str, "([^%w%.%- ])", urlencodeChar)
    -- convert spaces to "+" symbols
    return string.gsub(str, " ", "+")
end

function string.utf8len(str)
    local len  = #str
    local left = len
    local cnt  = 0
    local arr  = {0, 0xc0, 0xe0, 0xf0, 0xf8, 0xfc}
    while left ~= 0 do
        local tmp = string.byte(str, -left)
        local i   = #arr
        while arr[i] do
            if tmp >= arr[i] then
                left = left - i
                break
            end
            i = i - 1
        end
        cnt = cnt + 1
    end
    return cnt
end

function string.formatNumberThousands(num)
    local formatted = tostring(tonumber(num))
    local k
    while true do
        formatted, k = string.gsub(formatted, "^(-?%d+)(%d%d%d)", '%1,%2')
        if k == 0 then break end
    end
    return formatted
end

--获取路径
function string.ipfilename(filename)
    --return string.match(filename, "(.+)/[^/]*%.%w+$") --*nix system
    return string.match(filename, "(.+)\\[^\\]*%.%w+$") -- windows
end

--获取文件名
function string.ippath(filename)
    --return string.match(filename, ".+/([^/]*%.%w+)$") -- *nix system
    return string.match(filename, ".+\\([^\\]*%.%w+)$") -- *windows system
end

--去除扩展名
function string.ipextension(filename)
    local idx = filename:match(".+()%.%w+$")
    if(idx) then
        return filename:sub(1, idx-1)
    else
        return filename
    end
end

--获取扩展名
function string.getextension(filename)
    return filename:match(".+%.(%w+)$")
end

function reverse( var )
    if var == true then
        var = false
    else
        var = true
    end
    return var
end